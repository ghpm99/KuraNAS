SELECT
    hf.id,
    hf."name",
    hf."path",
    hf.parent_path,
    hf.format,
    hf."size",
    hf.updated_at,
    hf.created_at,
    hf.last_interaction,
    hf.last_backup,
    hf."type",
    hf.checksum,
    hf.deleted_at,
    hf.starred,
    im.id,
    im.file_id,
    im."path",
    im.FORMAT,
    im."mode",
    im.WIDTH,
    im.HEIGHT,
    im.dpi_x,
    im.dpi_y,
    im.x_resolution,
    im.y_resolution,
    im.resolution_unit,
    im.orientation,
    im."compression",
    im.photometric_interpretation,
    im.color_space,
    im.components_configuration,
    im.icc_profile,
    im.make,
    im.model,
    im.software,
    im.lens_model,
    im.serial_number,
    im.datetime,
    im.datetime_original,
    im.datetime_digitized,
    im.subsec_time,
    im.exposure_time,
    im.f_number,
    im.iso,
    im.shutter_speed,
    im.aperture_value,
    im.brightness_value,
    im.exposure_bias,
    im.metering_mode,
    im.flash,
    im.focal_length,
    im.white_balance,
    im.exposure_program,
    im.max_aperture_value,
    im.gps_latitude,
    im.gps_longitude,
    im.gps_altitude,
    im.gps_date,
    im.gps_time,
    im.image_description,
    im.user_comment,
    im.copyright,
    im.artist,
    im.created_at
FROM
    home_file hf
    LEFT JOIN image_metadata im ON hf.id = im.file_id
WHERE
    hf.format = ANY($1)
    AND hf.deleted_at IS NULL
ORDER BY
    {{ORDER_BY}}
LIMIT
    $2
OFFSET
    $3;
